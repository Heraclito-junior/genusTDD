    private void buttonAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAdicionarProdutoActionPerformed
        // TODO add your handling code here:

        int codigo;
        String nome;
        int quantidade;
        int idFatura = fatura.getIDfatura();
        double preco;
        
        try {
            quantidade = Integer.parseInt(textFieldQuantidade.getText());
        }
        catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar produto Ã  fatura!\n"
                    + "Quantidade invÃ¡lida, insira valores inteiros.", "Erro ao adicionar produto Ã  fatura", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(quantidade <= 0){
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto!\n"
                    + "Quantidade invÃ¡lida, insira valores postivos.", "Erro ao adicionar produto", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (textFieldCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhum produto selecionado.");
            return;
        }
        
        try {
            codigo = Integer.parseInt(textFieldCodigo.getText());
        }
        catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar produto Ã  fatura!\n"
                    + "CÃ³digo invÃ¡lido.", "Erro ao adicionar produto Ã  fatura", JOptionPane.ERROR_MESSAGE);
            return;
        }

        nome = funcoes.retornarNomePorCodigo(codigo);
        preco = funcoes.inserirProdutoNaFatura(idFatura, idFornecedor, codigo, quantidade);
        
        if(preco == -1){
            JOptionPane.showMessageDialog(this, "Erro ao adicionar produto Ã  fatura!\n"
                + "Fornecedor nÃ£o trabalha com este produto.", "Erro ao adicionar produto", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        tableProdutosFatura.setValueAt(codigo,quantidadeProdutos,0);
        tableProdutosFatura.setValueAt(nome,quantidadeProdutos,1);
        tableProdutosFatura.setValueAt(quantidade,quantidadeProdutos,2);
        tableProdutosFatura.setValueAt(preco,quantidadeProdutos,3);
        quantidadeProdutos++;
        
        valorTotal += preco;
        valorTotal = Math.round(valorTotal * 100);
        valorTotal = valorTotal/100;
        
        textAreaValor.setText("R$ " + valorTotal);
        funcoes.atualizaFatura(idFatura, valorTotal, idFornecedor, idFuncionario);
        return;
        
    }//GEN-LAST:event_buttonAdicionarProdutoActionPerformed

